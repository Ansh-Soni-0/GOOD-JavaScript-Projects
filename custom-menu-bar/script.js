const addCountry = (selectedCountry) => {
    countryContainer.innerHTML = "";
    countries.map(country => {

        let isSelected = country == selectedCountry ? "selected" : "";

        let li = `<li onClick="updateName(this)" class="${isSelected}">${country}</li>`;
        countryContainer.insertAdjacentHTML("beforeend" , li);
    });
}

addCountry();

const updateName = (selectedLi) => {

    inputField.value = "";

    addCountry(selectedLi.innerHTML);
    // console.log(selectedLi.innerHTML);
    

    //in select button have two child therefore we excess only first child from (firstElementChild)
    selectBtn.firstElementChild.innerHTML = selectedLi.innerHTML
    wrapper.classList.remove("active");

    

    addCountry(selectedLi.innerHTML);
}

inputField.addEventListener("keyup" , () => {
    // console.log(inputField.value)
    let arr = [];
    const searchedValue = inputField.value;
    // console.log(searchedValue);

    arr = countries.filter(data => {
        return data.toLocaleLowerCase().startsWith(searchedValue) || data.startsWith(searchedValue) 
 
    })
    
    arr = arr.map((filterCountry) => `<li onClick="updateName(this)">${filterCountry}</li>`).join("")

    

    countryContainer.innerHTML = arr ? arr : `<p class="msg">Oops country not found! ðŸ˜Ÿ</p>`;

    
})





selectBtn.addEventListener("click", () => {
  wrapper.classList.toggle("active");
});
